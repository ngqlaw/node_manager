-module(node_manager_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case node_manager_sup:start_link() of
        {ok, Pid} ->
            node_manager:start(),
            {ok, Pid};
        Res ->
            Res
    end.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

